#!/usr/bin/env node
// Patch the generated Android project for release APK signing.
// Run AFTER `tauri android init` and `patch-android.mjs`.
// Usage: node scripts/patch-android-signing.mjs
//
// Reads signing config from environment variables:
//   ANDROID_KEYSTORE_BASE64 — base64-encoded .jks/.keystore file
//   ANDROID_KEYSTORE_PASSWORD
//   ANDROID_KEY_ALIAS
//   ANDROID_KEY_PASSWORD
//
// If ANDROID_KEYSTORE_BASE64 is not set, the script does nothing (debug builds
// use the default debug keystore and don't need this).

import { existsSync, writeFileSync, readFileSync } from "node:fs"
import { fileURLToPath } from "node:url"
import { dirname, join } from "node:path"

const root = join(dirname(fileURLToPath(import.meta.url)), "..")
const appDir = join(root, "src-tauri", "gen", "android", "app")
const gradleFile = join(appDir, "build.gradle.kts")
const keystorePath = join(appDir, "release.keystore")
const signingScript = join(appDir, "signing.gradle.kts")

if (!existsSync(gradleFile)) {
  console.error("build.gradle.kts not found — run tauri android init first.")
  process.exit(0)
}

const keystoreB64 = process.env.ANDROID_KEYSTORE_BASE64
if (!keystoreB64) {
  console.log("ANDROID_KEYSTORE_BASE64 not set — release signing skipped.")
  process.exit(0)
}

const storePassword = process.env.ANDROID_KEYSTORE_PASSWORD
const keyAlias = process.env.ANDROID_KEY_ALIAS
const keyPassword = process.env.ANDROID_KEY_PASSWORD

if (!storePassword || !keyAlias || !keyPassword) {
  console.error(
    "ANDROID_KEYSTORE_PASSWORD, ANDROID_KEY_ALIAS and ANDROID_KEY_PASSWORD must all be set for release signing.",
  )
  process.exit(1)
}

// Decode the keystore from the base64 secret.
writeFileSync(keystorePath, Buffer.from(keystoreB64, "base64"))
console.log(`Keystore written to ${keystorePath}`)

// Write a self-contained signing script that the main build.gradle.kts will
// apply.  Kept in a separate file so the patch on build.gradle.kts is just a
// single `apply(from = …)` line — easy to add and easy to spot.
writeFileSync(
  signingScript,
  `// Generated by scripts/patch-android-signing.mjs — release APK signing.
// Applied from build.gradle.kts.  Does nothing when the keystore file is
// absent (debug / unsigned builds).

val keystoreFile = file("release.keystore")
if (keystoreFile.exists()) {
    val env = System.getenv()
    val storePassword = env["ANDROID_KEYSTORE_PASSWORD"] ?: ""
    val keyAlias      = env["ANDROID_KEY_ALIAS"]      ?: ""
    val keyPassword   = env["ANDROID_KEY_PASSWORD"]   ?: ""

    android {
        signingConfigs {
            create("release") {
                this.storeFile = keystoreFile
                this.storePassword = storePassword
                this.keyAlias = keyAlias
                this.keyPassword = keyPassword
            }
        }
        buildTypes {
            getByName("release") {
                signingConfig = signingConfigs.getByName("release")
            }
        }
    }
}
`,
)
console.log(`Signing script written to ${signingScript}`)

// Append the apply line to build.gradle.kts.
let gradle = readFileSync(gradleFile, "utf-8")
if (!gradle.includes('apply(from = "signing.gradle.kts")')) {
  gradle = gradle.trimEnd() + '\n\napply(from = "signing.gradle.kts")\n'
  writeFileSync(gradleFile, gradle)
  console.log("build.gradle.kts patched — signing.gradle.kts will be applied.")
} else {
  console.log("build.gradle.kts already references signing.gradle.kts.")
}

console.log("Android release signing configured.")
